import smbus

class ADS1115:
    i2c = None

    # Pointer register
    REG_POINTER_MASK        = 0x03
    REG_POINTER_CONVERT     = 0x00
    REG_POINTER_CONFIG      = 0x01
    REG_POINTER_LOWTHRESH   = 0x02
    REG_POINTER_HITHRESH    = 0x03

    # Config register
    REG_CONFIG_OS_MASK      = 0x8000
    REG_CONFIG_OS_SINGLE    = 0x8000
    REG_CONFIG_OS_BUSY      = 0x0000
    REG_CONFIG_OS_NOTBUSY   = 0x8000

    REG_CONFIG_MUX_MASK     = 0x7000
    REG_CONFIG_MUX_DIFF_0_1 = 0x0000
    REG_CONFIG_MUX_DIFF_0_3 = 0x1000
    REG_CONFIG_MUX_DIFF_1_3 = 0x2000
    REG_CONFIG_MUX_DIFF_2_3 = 0x3000
    REG_CONFIG_MUX_DIFF_0_G = 0x4000
    REG_CONFIG_MUX_DIFF_1_G = 0x5000
    REG_CONFIG_MUX_DIFF_2_G = 0x6000
    REG_CONFIG_MUX_DIFF_3_G = 0x7000

    REG_CONFIG_PGA_MASK     = 0x0E00
    REG_CONFIG_PGA_6144     = 0x0000
    REG_CONFIG_PGA_4096     = 0x0200
    REG_CONFIG_PGA_2048     = 0x0400 # default
    REG_CONFIG_PGA_1024     = 0x0600
    REG_CONFIG_PGA_0512     = 0x0800
    REG_CONFIG_PGA_0256     = 0x0A00
    
    REG_CONFIG_MODE_MASK    = 0x0100
    REG_CONFIG_MODE_CONT    = 0x0000
    REG_CONFIG_MODE_SING    = 0x0100 # default

    REG_CONFIG_DR_MASK      = 0x00E0
    REG_CONFIG_DR_8         = 0x0000
    REG_CONFIG_DR_16        = 0x0020
    REG_CONFIG_DR_32        = 0x0040
    REG_CONFIG_DR_64        = 0x0060
    REG_CONFIG_DR_128       = 0x0080
    REG_CONFIG_DR_250       = 0x00A0 # default
    REG_CONFIG_DR_475       = 0x00C0
    REG_CONFIG_DR_860       = 0x00E0

    REG_CONFIG_CMODE_MASK   = 0x0010
    REG_CONFIG_CMODE_TRAD   = 0x0000 # default
    REG_CONFIG_CMODE_WINDOW = 0x0010

    REG_CONFIG_CPOL_MASK    = 0x0008
    REG_CONFIG_CPOL_ACTLOW  = 0x0000 # ALERT/RDY is low when active (default)
    REG_CONFIG_CPOL_ACTHI   = 0x0008 # ALERT/RDY is high when active

    REG_CONFIG_CLAT_MASK    = 0x0004
    REG_CONFIG_CLAT_NONLAT  = 0x0000 # Non-latching (default)
    REG_CONFIG_CLAT_LATCH   = 0x0004

    REG_CONFIG_CQUE_MASK    = 0x0003
    REG_CONFIG_CQUE_1CONV   = 0x0000
    REG_CONFIG_CQUE_2CONV   = 0x0001
    REG_CONFIG_CQUE_4CONV   = 0x0002
    REG_CONFIG_CQUE_DISBL   = 0x0003 # diable comparator and put teh ALERT/RDY pin high
